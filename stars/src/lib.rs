pub fn stars(n: u32) -> String {
let mut res = String::new();
// if n == 0 {
//    return "*".to_string() ; 
// }
// if n == 1 {
//    return "**".to_string() ; 
// }
for _ in 0..(2 as usize).pow(n)  {
   res.push_str("*"); 
}
res
}


#[test]
fn test_stars() {
    assert_eq!(stars(0), "*");
    assert_eq!(stars(1), "**");
    assert_eq!(stars(2), "****");
    assert_eq!(stars(3), "********");
    assert_eq!(stars(4), "****************");
    assert_eq!(stars(5), "********************************");
    assert_eq!(
        stars(10),
        "****************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************"
    );
}